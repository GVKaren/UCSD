    int getSimilarPatient(Patient p)
    {
        List<Patient> candidatos= new ArrayList<Patient>();
         List<Patient> localidad= new ArrayList<Patient>();
        for (Patient ran: waitinglist)
        {
            if (ran.urgency==p.urgency && ran.id==Variables.Fantasma )
                candidatos.add(ran);
        }
        if (candidatos.isEmpty())
        {
           for (Patient ran: waitinglist)
            {
                if (ran.urgency>=p.urgency && ran.id==Variables.Fantasma )
                    candidatos.add(ran);
            } 
           if (candidatos.size()>1)
           {
               Collections.sort(candidatos, (Patient a1, Patient a2) -> a1.urgency-a2.urgency);
               
           }
           return waitinglist.indexOf(candidatos.get(0));
        }
        
         if(candidatos.size()>1)
            {    for (Patient ran : candidatos)
                {
                    if(ran.location== p.location && ran.ta>= p.ta)
                    {
                        localidad.add(ran);

                    }
                }
                if(localidad.size()>0)
                    candidatos=localidad;
            }
            if (candidatos.size()>1)
                Collections.sort(candidatos, (Patient a1, Patient a2) -> a1.ta-a2.ta);
            if (candidatos.isEmpty())
                return -1;
           
        
         return waitinglist.indexOf(candidatos.get(0));
    }
