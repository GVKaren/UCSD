    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Problem AssigmentP = new Problem(Variables.variables, Variables.objetives);
        Run run = new Run();
        int[] permutation= new int[Variables.npatients];
        
        try {
            permutation = run.Start(AssigmentP);
        } catch (Exception ex) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i=0; i<permutation.length; i++)
        {
            int idPatient=permutation[i+Variables.fixedPatients];
            int waiting= i%Variables.maxTime;
            Patient patient= waitinglist.get(idPatient);
            int equip=(int) Math.floor(i/Variables.maxTime);
            SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
            String dt= patient.da;
                      
            Calendar c = Calendar.getInstance();
             try {
                 c.setTime(dFormat.parse(dt));
             } catch (ParseException ex) {
                 Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
             }
            c.add(Calendar.DATE, waiting);  // number of days to add
            dt = dFormat.format(c.getTime());  // dt is now the new date
            //4 - fecha
            jTable1.setValueAt(dt, idPatient, 4);
            //5 eqipo
            jTable1.setValueAt(equip, idPatient, 5);
            //6 Lugar
            jTable1.setValueAt("Localidad "+equips[equip].localization, idPatient, 6);
        }
        
        Variables.fixedPatients+=Variables.unmoviledate;
    }//GEN-LAST:event_jButton1ActionPerformed

